
#for loop

#for loop with strings
for character in "Instagram":
    print(character,end=" ")
print("")
#for loop with lists
friends = ["Insta", "fb","google"]
for friend in friends:
    print(friend,end=" ")
print("")
#for in range
for i in range(10):
    print(i,end=" ")
print("")
for i in range(3,10):
    print(i,end=" ")

#logic for
print("")
