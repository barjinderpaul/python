

#number conversion

#decimal to binary
num = 25
bin_num = bin(num)
print(bin_num)
print(bin_num[2:])

#binary to decimal
binary = 0b11001
print(binary) 

#decimal to octal :
num = 25
num_oct = oct(num)
print(num_oct)

#decimal to hex :
num = 25
num_hex = hex(num)
print(num_hex)

#hex to decimal :
print(0x19)